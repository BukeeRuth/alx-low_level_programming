#include <stdio.h>

/**
 * add - add two integers
 * @a: first int
 * @b: second int
 * return: sum
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * mul - multiply two integers
 * @a: first int
 * @b: second int
 * return: multiplication
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * return: division
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two integers
 * @a: first int
 * @b: second int
 * return: modulus
 */

int mod(int a, int b)
{
	return (a % b);
}

/**
 * sub - subtract two integers
 * @a: first int
 * @b: second int
 * return: subtraction
 */

int sub(int a, int b)
{
	return (a - b);
}
